let convertMaticUsd = async (matic) => {
  let balance = 0;
  try {
    var response = await axios.get(
      "https://api.binance.com/api/v3/ticker/price?symbol=MATICUSDT"
    );
    let a = response.data.price;

    balance = (parseFloat(matic) * parseFloat(a)) / 1;
    return balance;
  } catch (error) {
    console.log(error);
    return balance;
  }
};

