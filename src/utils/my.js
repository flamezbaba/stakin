let getCurrentProvider = () => {
  if (!window.web3) return "unknown";

  if (window.ethereum.isMetaMask) return "metamask";

  if (window.ethereum.isTrust) return "trustwallet";

  if (window.ethereum.isGoWallet) return "goWallet";

  if (window.ethereum.isAlphaWallet) return "alphaWallet";

  if (window.ethereum.isStatus) return "status";

  if (window.ethereum.isToshi) return "coinbase";

  if (typeof window.__CIPHER__ !== "undefined") return "cipher";

  if (window.ethereum.constructor.name === "EthereumProvider") return "mist";

  if (window.ethereum.constructor.name === "Web3FrameProvider") return "parity";

  if (window.ethereum.host && window.ethereum.host.indexOf("infura") !== -1)
    return "infura";

  if (window.ethereum.host && window.ethereum.host.indexOf("localhost") !== -1)
    return "localhost";

  return "unknown";
};

